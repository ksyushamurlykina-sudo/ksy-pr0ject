async def safe_edit(q, text, **kwargs):
    """edit_message_text що мовчки ігнорує 'Message is not modified'."""
    try:
        await q.edit_message_text(text, **kwargs)
    except Exception as e:
        if "Message is not modified" not in str(e):
            raise

async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q = update.callback_query
    await q.answer()
    uid   = q.from_user.id
    uname = q.from_user.username or str(uid)
    u     = get_user(uid)
    data  = q.data

    # --- Дозвіл/відхилення перезапуску (адмін) ---
    if data.startswith("restart_yes_"):
        target_uid = int(data.replace("restart_yes_", ""))
        pending_restart_requests.discard(target_uid)
        set_user(target_uid, {
            "day": 0, "step": None, "difficulty": "",
            "done_today": False,
            "step_since": today_iso(),
            "last_done": "",
            "inactive": False,
        })
        await context.bot.send_message(
            chat_id=target_uid,
            text="Перезапуск підтверджено. Починаємо спочатку!",
        )
        await context.bot.send_message(
            chat_id=target_uid,
            text=day_text(0),
            parse_mode="Markdown",
            reply_markup=start_kb(),
        )
        await safe_edit(q, "✅ Перезапуск дозволено. Прогрес скинуто.")
        return

    if data.startswith("restart_no_"):
        target_uid = int(data.replace("restart_no_", ""))
        pending_restart_requests.discard(target_uid)
        await context.bot.send_message(
            chat_id=target_uid,
            text="Запит на перезапуск відхилено. Ваш прогрес збережено — продовжуйте з того місця, де зупинились.",
        )
        await safe_edit(q, "❌ Перезапуск відхилено.")
        return

    # --- Вибіркова розсилка (адмін) ---
    if data.startswith("sendone_"):
        target_uid = int(data.replace("sendone_", ""))
        state = load_state()
        s = state.get(str(target_uid))
        if not s:
            await q.answer("Юзера не знайдено.", show_alert=True)
            return
        day = s.get("day", 0)
        if not (1 <= day <= 24):
            await q.answer("Юзер не в активній програмі.", show_alert=True)
            return
        try:
            await context.bot.send_message(
                chat_id=target_uid,
                text=day_text(day),
                parse_mode="Markdown",
            )
            await q.answer(f"Надіслано день {day}.", show_alert=True)
        except Exception as e:
            await q.answer(f"Помилка: {e}", show_alert=True)
        return

    # --- Пропустити фідбек ---
    if data == "skip_feedback":
        day = u.get("day", 0)
        difficulty = u.get("difficulty", "")
        save_feedback(uid, uname, day, "так", difficulty, "")
        set_step(u, None)
        mark_done(u)
        u["done_today"] = True
        u["day"]        = min(day + 1, 25)
        set_user(uid, u)
        reaction = random.choice(FEEDBACK_SKIP_REACTIONS)
        await safe_edit(q, completion_text(day, reaction))
        await send_after_day_message(context.bot, uid, day)
        if day == 3:
            await _send_day4(context.bot, uid)
        return

    # --- Кнопка «Почати» після /start ---
    if data == "begin":
        if u["day"] == 0:
            u["day"] = 1
            u["done_today"] = False
            set_user(uid, u)
        try:
            await safe_edit(q, day_text(1), parse_mode="Markdown")
        except Exception as e:
            if "Message is not modified" not in str(e):
                raise
        return

    # --- Навігація по днях (перегляд) ---
    if data.startswith("nav_"):
        view_day = int(data.replace("nav_", ""))
        current  = u["day"]
        cap = min(current, 24)  # не пускаємо на неіснуючий день 25
        if view_day > cap or view_day < 1:
            return
        buttons = []
        if view_day > 1:
            buttons.append(
                InlineKeyboardButton(
                    f"< День {view_day - 1}", callback_data=f"nav_{view_day - 1}"
                )
            )
        if view_day < cap:
            buttons.append(
                InlineKeyboardButton(
                    f"День {view_day + 1} >", callback_data=f"nav_{view_day + 1}"
                )
            )
        kb = InlineKeyboardMarkup([buttons]) if buttons else None
        await safe_edit(q,
            day_text(view_day), parse_mode="Markdown", reply_markup=kb
        )
        return

    # --- Оцінка складності ---
    if data.startswith("diff_"):
        emoji = data.replace("diff_", "")
        u["difficulty"] = f"{emoji} {DIFFICULTY_LABELS.get(emoji, '')}"
        set_step(u, "awaiting_feedback")
        set_user(uid, u)
        day_done = u.get("day", 0)
        prompt = REFLECT_PROMPTS.get(day_done, "Що ви помітили сьогодні?")
        if day_done >= 24:
            tail = "Дякую, що ділилися весь місяць — ваші нотатки справді допомагали мені бачити, як іде шлях у кожного."
        else:
            tail = REFLECT_TAILS[day_done % len(REFLECT_TAILS)]
        await safe_edit(q,
            f"Зафіксувала: {u['difficulty']}\n\n"
            f"{prompt}\n\n{tail}",
            reply_markup=InlineKeyboardMarkup([[
                InlineKeyboardButton("Пропустити", callback_data="skip_feedback"),
            ]]),
        )
        return

    # --- Вибір практики у «день вибору» ---
    if data.startswith("pick_"):
        parts = data.split("_")
        d, i = int(parts[1]), int(parts[2])
        opts = CHOICE_OPTIONS.get(d, [])
        if 0 <= i < len(opts):
            label = opts[i]
            save_feedback(uid, uname, d, "вибір практики", "", "", choice=label)
            await safe_edit(q,
                f"Чудово — сьогодні ваша практика: «{label}».\n\n"
                "Поверніться до неї протягом дня. Коли виконаєте — надішліть /done."
            )
        return

    # --- Вечірня перевірка ---
    if data.startswith("ev_"):
        parts = data.split("_")
        done  = parts[1] == "yes"
        day   = int(parts[2])

        if done:
            set_step(u, "awaiting_difficulty")
            set_user(uid, u)
            await safe_edit(q,
                "Як вам вдалося сьогоднішнє завдання?", reply_markup=diff_kb()
            )
        else:
            save_feedback(uid, uname, day, "ні", "", "")
            await safe_edit(q,
                "Нічого страшного. Завтра надішлю це ж завдання ще раз."
            )

