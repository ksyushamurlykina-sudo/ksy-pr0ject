async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    uid   = update.effective_user.id
    uname = update.effective_user.username or str(uid)
    text  = update.message.text

    # Адмін відповідає reply → пересилаємо учаснику
    if uid == ADMIN_ID and update.message.reply_to_message:
        target = pending_replies.get(update.message.reply_to_message.message_id)
        # Fallback: витягуємо id з тексту пересланого фідбеку (переживає рестарт)
        if not target:
            src_text = update.message.reply_to_message.text or ""
            m = re.search(r"id:\s*(\d+)", src_text)
            if m:
                target = int(m.group(1))
        if target:
            try:
                await context.bot.send_message(
                    chat_id=target,
                    text=f"Відповідь:\n\n{text}",
                )
                await update.message.reply_text("Відповідь надіслано.")
            except Exception as e:
                await update.message.reply_text(f"Помилка: {e}")
        else:
            await update.message.reply_text(
                "Не вдалося визначити учасника. "
                "Відповідайте reply саме на пересланий фідбек."
            )
        return

    u   = get_user(uid)
    day = u.get("day", 0)

    # Якщо юзер писав — він не заблокував бота; скидаємо прапорець
    if u.get("inactive"):
        u["inactive"] = False
        set_user(uid, u)

    # Очікуємо натискання кнопки складності — нагадаємо
    if u.get("step") == "awaiting_difficulty":
        await update.message.reply_text(
            "Будь ласка, спершу оцініть складність кнопкою вище 👆",
            reply_markup=diff_kb(),
        )
        return

    # Фідбек після вибору складності
    if u.get("step") == "awaiting_feedback":
        feedback   = "" if text.strip() == "–" else text
        difficulty = u.get("difficulty", "")
        save_feedback(uid, uname, day, "так", difficulty, feedback)
        set_step(u, None)
        mark_done(u)
        u["done_today"] = True
        u["day"]        = min(day + 1, 25)
        set_user(uid, u)

        if feedback:
            fwd = await context.bot.send_message(
                chat_id=ADMIN_ID,
                text=(
                    f"Фідбек — день {day}\n"
                    f"@{uname} (id: {uid})\n"
                    f"Складність: {difficulty}\n\n"
                    f"{feedback}\n\n"
                    f"Відповідайте reply на це повідомлення."
                ),
            )
            pending_replies[fwd.message_id] = uid
            reaction = random.choice(FEEDBACK_REACTIONS)
        else:
            reaction = random.choice(FEEDBACK_SKIP_REACTIONS)

        await update.message.reply_text(completion_text(day, reaction))
        await send_after_day_message(context.bot, uid, day)
        if day == 3:
            await _send_day4(context.bot, uid)
        return

    # Вільне повідомлення → пересилаємо адміну
    save_feedback(uid, uname, day, "повідомлення", "", text)
    fwd = await context.bot.send_message(
        chat_id=ADMIN_ID,
        text=(
            f"Повідомлення — день {day}\n"
            f"@{uname} (id: {uid})\n\n"
            f"{text}\n\n"
            f"Відповідайте reply на це повідомлення."
        ),
    )
    pending_replies[fwd.message_id] = uid
    await update.message.reply_text("Дякую! Я отримаю ваше повідомлення і відповім особисто.")

