def day_text(n: int) -> str:
    d = DAYS.get(n)
    if not d:
        return "Програму завершено. Дякуємо за участь!"
    suffix = "\n\n_Коли виконаєте — надішліть /done_" if n >= 1 else ""
    return f"*{d['badge']}*\n\n*{d['title']}*\n\n{d['text']}{suffix}"

def diff_kb():
    return InlineKeyboardMarkup([
        [
            InlineKeyboardButton("☠️ нестерпно", callback_data="diff_☠️"),
            InlineKeyboardButton("👿 напряжно", callback_data="diff_👿"),
        ],
        [
            InlineKeyboardButton("😳 нормально", callback_data="diff_😳"),
            InlineKeyboardButton("❤️ легко", callback_data="diff_❤️"),
        ],
    ])

def eve_kb(day: int):
    return InlineKeyboardMarkup([[
        InlineKeyboardButton("Виконав/ла", callback_data=f"ev_yes_{day}"),
        InlineKeyboardButton("Не виконав/ла", callback_data=f"ev_no_{day}"),
    ]])

def start_kb():
    return InlineKeyboardMarkup([[
        InlineKeyboardButton("Почати", callback_data="begin"),
    ]])

def choice_kb(day: int):
    opts = CHOICE_OPTIONS.get(day, [])
    return InlineKeyboardMarkup(
        [[InlineKeyboardButton(o, callback_data=f"pick_{day}_{i}")] for i, o in enumerate(opts)]
    )

def completion_text(day: int, reaction: str) -> str:
    """Формує повідомлення після завершення дня.
    Для дня 24 — коротке фінальне привітання (без опитування)."""
    if day >= 24:
        return (
            f"{reaction}\n\n"
            "🎉 Ви завершили 24-денну програму! Це був насичений місяць — "
            "дякую, що пройшли його до кінця. 🤍"
        )
    return f"{reaction}\n✅ День {day} виконано! Завтра о 09:35 — завдання дня {day + 1}."

