async def cmd_day(update: Update, context: ContextTypes.DEFAULT_TYPE):
    u = get_user(update.effective_user.id)
    day = u["day"]
    if day < 1:
        await update.message.reply_text(
            "Програма ще не почалась. Перше завдання прийде завтра о 09:35."
        )
        return
    kb = None
    if day > 1:
        kb = InlineKeyboardMarkup([[
            InlineKeyboardButton(
                "Переглянути попередні дні", callback_data=f"nav_{min(day, 24) - 1}"
            ),
        ]])
    await update.message.reply_text(
        day_text(min(day, 24)), parse_mode="Markdown",
        reply_markup=(choice_kb(min(day, 24)) if DAYS[min(day, 24)].get("choice") else kb)
    )

async def cmd_done(update: Update, context: ContextTypes.DEFAULT_TYPE):
    uid = update.effective_user.id
    u = get_user(uid)

    if u["day"] < 1:
        await update.message.reply_text("Програма ще не почалась — завдань поки нема.")
        return
    if u["day"] > 24:
        await update.message.reply_text("Програму вже завершено!")
        return
    if u.get("step") in ("awaiting_difficulty", "awaiting_feedback"):
        await update.message.reply_text(
            "Ви вже в процесі зворотного зв'язку — завершіть його, будь ласка."
        )
        return
    if u.get("done_today"):
        await update.message.reply_text(
            "Ви вже позначили сьогоднішнє завдання як виконане."
        )
        return

    set_step(u, "awaiting_difficulty")
    set_user(uid, u)
    await update.message.reply_text(
        "Як вам вдалося сьогоднішнє завдання?", reply_markup=diff_kb()
    )

async def cmd_progress(update: Update, context: ContextTypes.DEFAULT_TYPE):
    u = get_user(update.effective_user.id)
    day = u["day"]
    if day < 1:
        await update.message.reply_text(
            "Програма ще не почалась. Перше завдання прийде завтра о 09:35."
        )
        return
    completed = max(0, day - 1) if day <= 24 else 24
    await update.message.reply_text(
        f"Прогрес: *{completed}/24* днів завершено. Поточний день: *{min(day, 24)}*",
        parse_mode="Markdown",
    )

async def cmd_stats(update: Update, context: ContextTypes.DEFAULT_TYPE):
    caller = update.effective_user.id
    logger.info(f"/stats від {caller}, ADMIN_ID={ADMIN_ID}, match={caller == ADMIN_ID}")
    if caller != ADMIN_ID:
        await update.message.reply_text("Ця команда доступна тільки адміну.")
        return
    state = load_state(force_refresh=True)
    if not state:
        await update.message.reply_text("Ще нема учасників.")
        return
    lines = [f"Учасників: {len(state)}\n"]
    for uid, s in state.items():
        flag = " 🔒" if s.get("inactive") else ""
        lines.append(f"  {uid} — день {s.get('day', 0)}/24{flag}")
    await update.message.reply_text("\n".join(lines))

