async def cmd_cleanup(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Адмін: масове чищення бази.
      /cleanup             — лише показати, кого буде видалено (нічого не чіпає)
      /cleanup confirm     — видалити заблокованих + завершених (день>24) + мовчунів (>=CLEANUP_STALE_DAYS дн)
      /cleanup all confirm — видалити ВСІХ, крім адміна (повний рестарт для нового набору)
    """
    if update.effective_user.id != ADMIN_ID:
        return
    args = {a.lower() for a in (context.args or [])}
    full = "all" in args
    do   = "confirm" in args
    state = load_state(force_refresh=True)

    to_remove = {}   # uid -> причина
    for uid, st in state.items():
        if int(uid) == ADMIN_ID:
            continue
        if full:
            to_remove[uid] = "all"; continue
        if st.get("inactive"):
            to_remove[uid] = "заблокував"; continue
        if st.get("day", 0) > 24:
            to_remove[uid] = "завершив"; continue
        anchor = st.get("last_done") or st.get("step_since")
        n = days_since(anchor)
        if n is not None and n >= CLEANUP_STALE_DAYS:
            to_remove[uid] = f"мовчить {n} дн"

    if not to_remove:
        await update.message.reply_text("Нікого не підпадає під чищення.")
        return

    counts = {}
    for r in to_remove.values():
        k = "all" if full else r.split()[0]
        counts[k] = counts.get(k, 0) + 1
    head = "🧹 ПОВНЕ чищення (всі, крім адміна)" if full else "🧹 Чищення"
    summary = f"{head}: {len(to_remove)} під видалення.\n" + \
              "\n".join(f"  - {k}: {v}" for k, v in counts.items())

    if not do:
        sample = "\n".join(f"  id:{u} - {r}" for u, r in list(to_remove.items())[:60])
        cmd = "/cleanup all confirm" if full else "/cleanup confirm"
        await update.message.reply_text(
            summary + "\n\n" + sample +
            f"\n\n(прев'ю, нічого не видалено). Щоб виконати: {cmd}"
        )
        return

    for uid in list(to_remove):
        state.pop(uid, None)
    save_state(state)
    await update.message.reply_text(
        summary + f"\n\nГотово: видалено {len(to_remove)}. Лишилось у базі: {len(state)}."
    )

async def cmd_inactive(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Адмін: дві секції — заблокували бота та мовчать 3+ днів."""
    if update.effective_user.id != ADMIN_ID:
        return
    state = load_state(force_refresh=True)
    blocked = [(uid, s) for uid, s in state.items() if s.get("inactive")]

    silent = []
    for uid, s in state.items():
        if s.get("inactive"):
            continue
        day = s.get("day", 0)
        if day < 1 or day > 24:
            continue
        # беремо last_done, якщо є; інакше — step_since як fallback
        anchor = s.get("last_done") or s.get("step_since")
        n = days_since(anchor)
        if n is not None and n >= 3:
            silent.append((uid, s, n, anchor))

    if not blocked and not silent:
        await update.message.reply_text("Усі активні — ніхто не заблокував і ніхто не мовчить 3+ днів.")
        return

    lines = []
    if blocked:
        lines.append(f"🔒 Заблокували бота ({len(blocked)}):")
        for uid, s in blocked:
            lines.append(f"  id:{uid} — день {s.get('day', 0)}/24")
        lines.append("")
    if silent:
        lines.append(f"😶 Мовчать 3+ днів ({len(silent)}):")
        for uid, s, n, anchor in sorted(silent, key=lambda x: -x[2]):
            lines.append(f"  id:{uid} — день {s.get('day', 0)}/24, останній раз {anchor} ({n} дн. тому)")
    await update.message.reply_text("\n".join(lines))

