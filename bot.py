async def job_morning(context: ContextTypes.DEFAULT_TYPE):
    state = load_state(force_refresh=True)
    for uid_str, s in state.items():
        day = s.get("day", 0)

        # Пропускаємо завершених і заблокованих
        if day > 24 or s.get("inactive"):
            continue

        # Якщо юзер застряг у фідбеці з попереднього дня — авто-закриваємо.
        # Семантика: складність уже могла бути обрана, фідбек опціональний,
        # тож просто фіксуємо «виконано без фідбеку» і просуваємо далі.
        if s.get("step") in ("awaiting_difficulty", "awaiting_feedback") and 1 <= day <= 24:
            difficulty = s.get("difficulty", "")
            try:
                save_feedback(int(uid_str), uid_str, day, "так (авто)", difficulty, "")
            except Exception as e:
                logger.error(f"Авто-закриття дня {day} для {uid_str}: {e}")
            s["last_done"] = s.get("step_since") or today_iso()
            s["day"] = min(day + 1, 25)
            day = s["day"]
            logger.info(f"Авто-закрито застряглий день у {uid_str} → переведено на день {day}")
            if day == 4:
                await _send_day4(context.bot, int(uid_str))

        if day == 0:
            s["day"] = 1
            day = 1

        s["done_today"] = False
        # скидаємо незавершений фідбек; фіксуємо дату переходу
        if s.get("step") is not None:
            s["step_since"] = today_iso()
        s["step"] = None
        state[uid_str] = s

        # День 4: завдання вже надіслано при завершенні дня 3 — вранці лише нагадування
        if day == 4:
            try:
                await context.bot.send_message(
                    chat_id=int(uid_str),
                    text="Доброго ранку 🌤 Нагадую: сьогодні — те ранкове завдання дня 4. Команда /day покаже його ще раз.",
                )
            except Exception as e:
                logger.warning(f"Ранок-нагадування д4 {uid_str}: {e}")
            continue

        if 1 <= day <= 24:
            try:
                await context.bot.send_message(
                    chat_id=int(uid_str),
                    text=day_text(day),
                    parse_mode="Markdown",
                    reply_markup=choice_kb(day) if DAYS[day].get("choice") else None,
                )
            except Exception as e:
                if "Chat not found" in str(e) or "bot was blocked" in str(e).lower() or "user is deactivated" in str(e).lower():
                    logger.warning(f"Ранок {uid_str}: неактивний юзер — {e}")
                    s["inactive"] = True
                    state[uid_str] = s
                else:
                    logger.error(f"Ранок {uid_str}: {e}")

    save_state(state)

