async def job_morning(context: ContextTypes.DEFAULT_TYPE):
    state = load_state(force_refresh=True)
    for uid_str, s in state.items():
        day = s.get("day", 0)

        # Пропускаємо завершених і заблокованих
        if day > 14 or s.get("inactive"):
            continue

        if day == 0:
            s["day"] = 1
            day = 1

        s["done_today"] = False
        # скидаємо незавершений фідбек; фіксуємо дату переходу
        if s.get("step") is not None:
            s["step_since"] = today_iso()
        s["step"] = None
        state[uid_str] = s

        if 1 <= day <= 14:
            try:
                await context.bot.send_message(
                    chat_id=int(uid_str),
                    text=day_text(day),
                    parse_mode="Markdown",
                )
            except Exception as e:
                if "Chat not found" in str(e) or "bot was blocked" in str(e).lower() or "user is deactivated" in str(e).lower():
                    logger.warning(f"Ранок {uid_str}: неактивний юзер — {e}")
                    s["inactive"] = True
                    state[uid_str] = s
                else:
                    logger.error(f"Ранок {uid_str}: {e}")

    save_state(state)

